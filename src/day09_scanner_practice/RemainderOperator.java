package day09_scanner_practice;

public class RemainderOperator {
    public static void main(String[] args){
        System.out.println(10/3);//3
        System.out.println(10%3);//1 remainder
                          // 3+3+3+1
        System.out.println(10%5);// no remainder //
                          // 5+5+0
        System.out.println(20/7);//7+7+6
        System.out.println(20%7);//6
                          //7+7+6
        System.out.println();
    }
}
