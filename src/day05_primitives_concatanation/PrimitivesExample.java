package day05_primitives_concatanation;

public class PrimitivesExample {
    public static void main(String[] args)
    {
        // WHOLE NUMBER/INTEGER TYPE
        byte bytevalue = 100;
        String message= "byte max = 127";
        System.out.println(message);
        System.out.println(bytevalue);

        short shortvalue=32000;
        System.out.println("short max= 32768");
        System.out.println(shortvalue);

        int intvalue=21235345;
        long longvalue=522345232323245L;
        System.out.println("long variable value->we put L at the end\n ");

        //DECIMAL NUMBERS /FLOATING POINT TYPE
        float floatValue= 2345.55F;
        double doubleValue=2355432.43340;//2355432.43340D also valid

        // CHARACTER TYPE
        char charValue='a';

        //BOOLEAN
        boolean booleanValue= true;
        //n double quote and no upper case letter
        System.out.println( intvalue);
        System.out.println( longvalue);
        System.out.println(floatValue);
        System.out.println(doubleValue);






    }



}
