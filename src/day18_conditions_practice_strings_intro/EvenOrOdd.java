package day18_conditions_practice_strings_intro;

public class EvenOrOdd {
    public static void main(String[] args){
        int num = 4;
        if(num % 2 == 0){  //divisable by 2
            System.out.println(num +" is even");
        }else{
            System.out.println(num+" is odd");
        }
    }

}
